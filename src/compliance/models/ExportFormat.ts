/**
 * Supported export formats
 */
export enum ExportFormat {
  /**
   * CSV format for tabular data
   */
  CSV = 'csv',
  
  /**
   * JSON format for structured data
   */
  JSON = 'json',
  
  /**
   * Excel spreadsheet format
   */
  EXCEL = 'excel',
  
  /**
   * PDF document format
   */
  PDF = 'pdf',
  
  /**
   * XML format for hierarchical data
   */
  XML = 'xml'
} 